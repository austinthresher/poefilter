def find(hue, value, chroma):
    match = ""
    dist = 1000
    for k in _munsell.keys():
        if k.startswith(hue):
            components = k.split("_")
            v = int(components[1])
            c = int(components[2]) 
            d = abs(v - value) + abs(c - chroma)
            if d < dist:
                dist = d
                match = k
    return match

def get(hue, value, chroma):
    key = hue + "_" + str(value) + "_" + str(chroma)
    try:
        rgb = _munsell[key]
    except:
        sys.stderr.write("color does not exist: ")
        sys.stderr.write("hue=" + hue + ", value=" + str(value) + ", chroma=" + str(chroma) + "\n")
        sys.stderr.write("similar match: ")
        similar = find(hue, value, chroma)
        if similar:
            sys.stderr.write(similar + "\n")
            rgb = _munsell[similar]
        else:
            sys.stderr.write("none\n")
            rgb = (0, 0, 0)
    return " ".join(str(x) for x in rgb)
